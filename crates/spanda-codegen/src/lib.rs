//! codegen support for Spanda.
//!
use spanda_ast::foundations::{ExternFnDecl, ModuleFnDecl};
use spanda_ast::nodes::*;
use spanda_error::SpandaError;
use spanda_sir::SirProgram;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum CodegenTarget {
    Native,
    Wasm,
    Esp32,
}

pub fn generate(source: &str, target: CodegenTarget) -> Result<String, SpandaError> {
    // Description:
    //     Generate.
    //
    // Inputs:
    //     source: &str
    //         Caller-supplied source.
    //     arge: CodegenTarget
    //         Caller-supplied arge.
    //
    // Outputs:
    //     result: Result<String, SpandaError>
    //         Return value from `generate`.
    //
    // Example:
    //     let result = spanda_codegen::generate(source, arge);

    // Tokenize, parse, then lower to SIR without requiring the full driver pipeline.
    let tokens = spanda_lexer::tokenize(source)?;
    let program = spanda_parser::parse(tokens)?;
    let sir = spanda_sir::lower_program(&program);
    Ok(render_from_sir(&sir, &program, target))
}

fn render_from_sir(sir: &SirProgram, program: &Program, target: CodegenTarget) -> String {
    // Description:
    //     Render from sir.
    //
    // Inputs:
    //     sir: &SirProgram
    //         Caller-supplied sir.
    //     progra: &Program
    //         Caller-supplied progra.
    //     arge: CodegenTarget
    //         Caller-supplied arge.
    //
    // Outputs:
    //     result: String
    //         Return value from `render_from_sir`.
    //
    // Example:
    //     let result = spanda_codegen::render_from_sir(sir, progra, arge);

    // Match on target and handle each case.
    match target {
        CodegenTarget::Native => render_native_sir(sir, program),
        CodegenTarget::Wasm => render_wasm_sir(sir),
        CodegenTarget::Esp32 => render_esp32(program),
    }
}

fn render_native_sir(sir: &SirProgram, program: &Program) -> String {
    // Description:
    //     Render native sir.
    //
    // Inputs:
    //     sir: &SirProgram
    //         Caller-supplied sir.
    //     progra: &Program
    //         Caller-supplied progra.
    //
    // Outputs:
    //     result: String
    //         Return value from `render_native_sir`.
    //
    // Example:
    //     let result = spanda_codegen::render_native_sir(sir, progra);

    // Hold the lowered SIR program for codegen.
    let mut out = String::from("// Generated by spanda codegen --target native (SIR v1)\n");
    out.push_str("#include <stdint.h>\n\n");

    // Emit output when module name provides a name.
    if let Some(name) = &sir.module_name {
        out.push_str(&format!("// module {name}\n"));
    }

    // Emit codegen metadata for each import.
    for import in &sir.imports {
        out.push_str(&format!("// import {import}\n"));
    }

    // Declare each extern function in the generated output.
    for ext in &sir.externs {
        out.push_str(&format!(
            "// extern {} fn {}(...) -> {}\n",
            ext.bridge.as_str(),
            ext.name,
            ext.return_type
        ));
    }

    // Generate code for each module function.
    for func in &sir.functions {
        out.push_str(&format!(
            "int32_t {}(/* {} params */);\n",
            func.name,
            func.params.len()
        ));
    }

    // Handle each robot declared in the program.
    for robot in &sir.robot_names {
        out.push_str(&format!("void spanda_robot_{robot}_init(void);\n"));
        let behaviors: Vec<_> = sir
            .behavior_names
            .iter()
            .filter(|_| true)
            .cloned()
            .collect();

        // Skip further work when !behaviors is empty.
        if !behaviors.is_empty() {
            out.push_str(&format!(
                "// robot {robot} behaviors: {}\n",
                behaviors.join(", ")
            ));
        }
    }
    let Program::Program {
        extern_functions, ..
    } = program;

    // Declare each extern function in the generated output.
    for ext in extern_functions {
        out.push_str(&render_extern_c(ext));
    }
    out.push_str("\nint32_t spanda_main(void) {\n  return 0;\n}\n");
    out
}

fn render_wasm_sir(sir: &SirProgram) -> String {
    // Description:
    //     Render wasm sir.
    //
    // Inputs:
    //     sir: &SirProgram
    //         Caller-supplied sir.
    //
    // Outputs:
    //     result: String
    //         Return value from `render_wasm_sir`.
    //
    // Example:
    //     let result = spanda_codegen::render_wasm_sir(sir);

    // Hold the lowered SIR program for codegen.
    let mut out = String::from(";; Generated by spanda codegen --target wasm (SIR v1)\n");
    out.push_str("(module\n");

    // Emit output when module name provides a name.
    if let Some(name) = &sir.module_name {
        out.push_str(&format!("  ;; module {name}\n"));
    }

    // Declare each extern function in the generated output.
    for ext in &sir.externs {
        out.push_str(&format!(
            "  (import \"env\" \"{}\" (func ${}))\n",
            ext.name, ext.name
        ));
    }

    // Generate code for each module function.
    for func in &sir.functions {
        out.push_str(&format!(
            "  (func ${} (export \"{}\") (result i32) i32.const 0)\n",
            func.name, func.name
        ));
    }
    out.push_str("  (func (export \"spanda_main\") (result i32) i32.const 0)\n");
    out.push_str(")\n");
    out
}

fn render_esp32(program: &Program) -> String {
    // Description:
    //     Render esp32.
    //
    // Inputs:
    //     progra: &Program
    //         Caller-supplied progra.
    //
    // Outputs:
    //     result: String
    //         Return value from `render_esp32`.
    //
    // Example:
    //     let result = spanda_codegen::render_esp32(progra);

    // Destructure the program into its top-level sections.
    let Program::Program {
        module_name,
        robots,
        ..
    } = program;
    let mut out = String::from("// Generated by spanda codegen --target esp32\n");
    out.push_str("#include <Arduino.h>\n\n");

    // Emit output when module name provides a name.
    if let Some(name) = module_name {
        out.push_str(&format!("// module {name}\n"));
    }
    out.push_str("void setup() {\n  Serial.begin(115200);\n}\n\n");
    out.push_str("void loop() {\n");

    // Handle each robot declared in the program.
    for robot in robots {
        let RobotDecl::RobotDecl { name, .. } = robot;
        out.push_str(&format!("  // robot {name}\n"));
    }
    out.push_str("  delay(100);\n}\n");
    out
}

fn render_extern_c(ext: &ExternFnDecl) -> String {
    // Description:
    //     Render extern c.
    //
    // Inputs:
    //     ex: &ExternFnDecl
    //         Caller-supplied ex.
    //
    // Outputs:
    //     result: String
    //         Return value from `render_extern_c`.
    //
    // Example:
    //     let result = spanda_codegen::render_extern_c(ex);

    // Compute ret for the following logic.
    let ret = c_type(&ext.return_type);
    let params = ext
        .params
        .iter()
        .map(|p| format!("{} {}", c_type(&p.type_ann), p.name))
        .collect::<Vec<_>>()
        .join(", ");
    format!(
        "extern {} {}({});\n",
        ret,
        ext.name,
        // Skip further work when params is empty.
        if params.is_empty() {
            "void".into()
        } else {
            params
        }
    )
}

#[allow(dead_code)]
fn render_fn_decl_c(func: &ModuleFnDecl) -> String {
    // Description:
    //     Render fn decl c.
    //
    // Inputs:
    //     func: &ModuleFnDecl
    //         Caller-supplied func.
    //
    // Outputs:
    //     result: String
    //         Return value from `render_fn_decl_c`.
    //
    // Example:
    //     let result = spanda_codegen::render_fn_decl_c(func);

    // Compute ret for the following logic.
    let ret = c_type(&func.return_type);
    let params = func
        .params
        .iter()
        .map(|p| format!("{} {}", c_type(&p.type_ann), p.name))
        .collect::<Vec<_>>()
        .join(", ");
    format!(
        "{} {}({});\n",
        ret,
        func.name,
        // Skip further work when params is empty.
        if params.is_empty() {
            "void".into()
        } else {
            params
        }
    )
}

fn c_type(ty: &SpandaType) -> &'static str {
    // Description:
    //     C type.
    //
    // Inputs:
    //     y: &SpandaType
    //         Caller-supplied y.
    //
    // Outputs:
    //     result: &'static str
    //         Return value from `c_type`.
    //
    // Example:
    //     let result = spanda_codegen::c_type(y);

    // Match on ty and handle each case.
    match ty {
        SpandaType::Int => "int32_t",
        SpandaType::Float => "double",
        SpandaType::Bool => "bool",
        SpandaType::String => "const char*",
        SpandaType::Bytes => "const uint8_t*",
        SpandaType::Void => "void",
        _ => "int32_t",
    }
}

pub fn wasm_deploy_manifest(source: &str) -> Result<String, SpandaError> {
    // Description:
    //     Wasm deploy manifest.
    //
    // Inputs:
    //     source: &str
    //         Caller-supplied source.
    //
    // Outputs:
    //     result: Result<String, SpandaError>
    //         Return value from `wasm_deploy_manifest`.
    //
    // Example:
    //     let result = spanda_codegen::wasm_deploy_manifest(source);

    // Tokenize the source before parsing.
    let tokens = spanda_lexer::tokenize(source)?;
    let program = spanda_parser::parse(tokens)?;
    let Program::Program {
        module_name,
        functions,
        robots,
        ..
    } = &program;
    let manifest = serde_json::json!({
        "target": "wasm",
        "module": module_name,
        "exports": functions.iter().map(|f| &f.name).collect::<Vec<_>>(),
        "robots": robots.iter().map(|r| r.name()).collect::<Vec<_>>(),
        "toolchain": "wasm-pack",
        "entry": "spanda_main",
    });
    Ok(serde_json::to_string_pretty(&manifest).unwrap_or_else(|_| "{}".into()))
}
